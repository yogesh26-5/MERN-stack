//Data types in JS
//Premitive Data types
//1.Number
let a = 10
console.log(a)
//2.String
let b = "Hello"
console.log(b)
//3.Boolean
let c = true
console.log(c)
//4.Undefined
let d
console.log(d)
//5.Null
let e = null
console.log(e)
//6.Symbol
let f = Symbol("Hello")
console.log(f)
//7.BigInt
let g = BigInt(1234567890123456789012345678901234567890)
console.log(g)
//Non-Premitive Data types
//1.Object
let h = { name: "John", age: 20 }
console.log(h)
//2.Array
let i = [1, 2, 3, 4, 5]
console.log(i)
//3.Function
let j = function () {
    console.log("Hello")
}
console.log(j)